class Switch {
    public static void main (String [] args) {
      int val = 10;
      switch(val) {
      case 0:
	System.out.println("val = 0");
	break;
      case 1:
	System.out.println("val = 1");
	break;
      case 2:
	System.out.println("val = 2");
	break;
      case 3:
	System.out.println("val = 3");
	break;
      case 4:
	System.out.println("val = 4");
	break;
      case 5:
	System.out.println("val = 5");
	break;
      default:
	System.out.println("val > 5");
      }
    }
  }